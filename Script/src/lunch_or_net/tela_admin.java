    private void btnprodutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprodutosActionPerformed
        try {
            Connection connection = new ConnectionFactory().getConnection();
            String sql = "select *from cprodutos";
            PreparedStatement stmt = connection.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            DefaultTableModel modelo = (DefaultTableModel) tblprodutos.getModel();
            modelo.setNumRows(0);

            while (rs.next()) {
                modelo.addRow(new Object[]{rs.getString("id"), rs.getString("produto"), rs.getString("descricao"), rs.getString("preçocusto"), rs.getString("preçovenda"), rs.getString("categoria"), rs.getString("tipo")});
            }

            rs.close();
            connection.close();

        } catch (SQLException e1) {
            // TODO Auto-generated catch block
            e1.printStackTrace();
        }

        jTabbedPane1.setSelectedIndex(4);     }//GEN-LAST:event_btnprodutosActionPerformed

