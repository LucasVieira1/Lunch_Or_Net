package modelo;

public class Produto {
    String id;
    String produto;
    String descricao;
    String preçocusto;
    String preçovenda;
    String categoria;
    String tipo;
    String quantidade; 
    String precoatt;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProduto() {
        return produto;
    }

    public void setProduto(String produto) {
        this.produto = produto;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getPreçocusto() {
        return preçocusto;
    }

    public void setPreçocusto(String preçocusto) {
        this.preçocusto = preçocusto;
    }

    public String getPreçovenda() {
        return preçovenda;
    }

    public void setPreçovenda(String preçovenda) {
        this.preçovenda = preçovenda;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getPrecoatt() {
        return precoatt;
    }

    public void setPrecoatt(String precoatt) {
        this.precoatt = precoatt;
    }

   

    
    
    
    
}
