    private void btnatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnatualizarActionPerformed
        // para evitar erro siga as instruções do vídeo
        if (txtusuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe o usúario");
        } else if (!txtsenha.getText().equals(txtconfirmar.getText())) {
            JOptionPane.showMessageDialog(null, "As senhas não coincidem! ");
            txtsenha.setText("");
            txtconfirmar.setText("");
        } else {

            Usuario usuarios = new Usuario();
            usuarios.setEmail(txtemail.getText());
            usuarios.setSenha(txtsenha.getText());
            usuarios.setConfirmar(txtconfirmar.getText());
            usuarios.setNome(txtusuario.getText());
            UsuarioDAO dao = new UsuarioDAO();
            dao.update(usuarios);
            txtemail.setText("");
            txtsenha.setText("");
            txtconfirmar.setText("");
            txtid.setText("");
            desativaBotao2();

        }
    }//GEN-LAST:event_btnatualizarActionPerformed

